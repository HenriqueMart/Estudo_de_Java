    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        var valorA = Integer.parseInt(lblAA.getValue().toString());
        var valorB = Integer.parseInt(lblB.getValue().toString());
        var valorC = Integer.parseInt(lblC.getValue().toString());
        panel1.setVisible(true);
        
        double delta = (double) (Math.pow(valorB, 2)) -4 * (valorA * valorC);
        txtResultado.setText(Double.toString(delta));
        if(delta < 0){
            txtTipo.setText("NÃ£o Existe raizes reais para esse Delta");
        }else{
             txtTipo.setText("Existe raizes reais para esse valor");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

